// What is TypeScript?
// --------------------
// TypeScript is a strongly typed programming language that builds on JavaScript.
// It adds optional static types, classes, interfaces, and other features to help
// developers write safer and more maintainable code.

// Why use TypeScript over JavaScript?
// -----------------------------------
// 1. Type Safety: Helps catch errors at compile time instead of runtime.
// 2. Better Tooling: Enables autocompletion, refactoring, and real-time feedback.
// 3. Scalability: Ideal for large codebases and team collaboration.
// 4. Modern Features: Supports advanced features like interfaces, generics, and decorators.
// 5. Compatibility: TypeScript compiles to plain JavaScript, so it works anywhere JS does.

